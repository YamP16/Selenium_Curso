package com.java.Ejercicios;

public class Ejercicio4 {

	public static void main(String[] args) {
		//Comparación de valores con operadores relacionales
		
		int vara=36;
		int varb=31;
	
		if (vara > varb) {
			System.out.println("Variable A = "+vara+" es mayor a variable B = "+varb);
		}else if (vara == varb){
			System.out.println("Variable A = "+vara+" es igual a variable B = "+varb);
		}else {
			System.out.println("Variable A = "+vara+" es menor a variable B = "+varb);
		}

	}

}
